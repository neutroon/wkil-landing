const storageKey = "wkilLandingLocale";
const savedLocale = getSavedLocale();
let locale = content[savedLocale] ? savedLocale : "ar";

function getSavedLocale() {
  try {
    return localStorage.getItem(storageKey) || "ar";
  } catch {
    return "ar";
  }
}

function saveLocale(nextLocale) {
  try {
    localStorage.setItem(storageKey, nextLocale);
  } catch {
    /* Local files can be opened with storage disabled. */
  }
}

function current() {
  return content[locale];
}

function t(path) {
  return path.split(".").reduce((acc, key) => acc?.[key], current());
}

function signupPath() {
  return `/${locale}/auth/signup`;
}

function loginPath() {
  return `/${locale}/auth/login`;
}

function setStaticText() {
  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const value = t(node.dataset.i18n);
    if (Array.isArray(value)) {
      node.innerHTML = value.map((line) => `<span>${line}</span>`).join("");
    } else if (typeof value === "string") {
      node.textContent = value;
    }
  });

  document.getElementById("languageToggle").textContent =
    t("nav.switchLanguage");
}

function setLinks() {
  document.querySelectorAll("[data-signup]").forEach((node) => {
    node.setAttribute("href", signupPath());
  });

  document.querySelectorAll("[data-login]").forEach((node) => {
    node.setAttribute("href", loginPath());
  });
}

