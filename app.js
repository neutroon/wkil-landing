function renderServices() {
  document.getElementById("servicesGrid").innerHTML = current()
    .services.items.map(
      ([icon, title, text]) => `
        <article class="service-card">
          <div class="service-head">
            <div class="service-icon">${serviceIcon(icon)}</div>
            <h3>${title}</h3>
          </div>
          <p>${text}</p>
        </article>
      `,
    )
    .join("");
}

function renderWorkflow() {
  document.getElementById("workflowList").innerHTML = current()
    .workflow.steps.map(
      ([number, title, text]) => `
        <article class="workflow-item">
          <div class="step-number">${number}</div>
          <div>
            <h3>${title}</h3>
            <p>${text}</p>
          </div>
        </article>
      `,
    )
    .join("");
}

function render() {
  document.documentElement.lang = locale;
  document.documentElement.dir = locale === "ar" ? "rtl" : "ltr";
  document.title = locale === "ar" ? "وكيل | wkil" : "Wkil | وكيل";

  setStaticText();
  setLinks();
  renderAssurance();
  renderResults();
  renderTrust();
  renderServices();
  renderWorkflow();
}

document.getElementById("languageToggle").addEventListener("click", () => {
  locale = locale === "ar" ? "en" : "ar";
  saveLocale(locale);
  render();
});

render();
